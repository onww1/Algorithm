#include <cstdio>
int main(int argc, char *argv[]) {
  int n;
  scanf("%d", &n);
  if (n == 1) {
    printf("65\n");
  } else if (n == 2) {
    printf("17\n");
  } else if (n == 3) {
    printf("4\n");
  } else if (n == 4) {
    printf("4\n");
  } else if (n == 5) {
    printf("64\n");
  }
  return 0;
}

